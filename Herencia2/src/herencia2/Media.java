public class Media extends Ropa{
    String genero;

    public Media() {
    }

    public Media(String genero, int id, double precio) {
        super(id, precio);
        this.genero = genero;
    }

    public String getGenero() {
        return genero;
    }

    public void setGenero(String genero) {
        this.genero = genero;
    }
    
}
